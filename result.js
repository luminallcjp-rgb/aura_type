/**
 * result.htmlの初期化処理。
 * sessionStorageからデータを取得し、不正アクセス時はガイダンスを表示。
 * 正常時は診断中演出を再生し、その後結果を描画する。
 */
function initResultPage() {
  const diagnosisData = loadDiagnosisData();

  if (!diagnosisData) {
    showNoDataScreen();
    return;
  }

  // もう一度診断するボタンのイベント登録
  const retryButton = document.getElementById("retryButton");
  if (retryButton) {
    retryButton.addEventListener("click", handleRetry);
  }

  // 診断中演出を再生し、終了後に結果を描画する
  playLoadingAnimation(() => {
    renderResult(diagnosisData);
  });
}

/**
 * sessionStorageから診断データを取得し、検証する。
 * @returns {Object|null} 正しいデータが取得できた場合はオブジェクト、そうでなければnull
 */
function loadDiagnosisData() {
  let rawValue;
  try {
    rawValue = sessionStorage.getItem(STORAGE_KEY);
  } catch (error) {
    // sessionStorageへアクセスできない環境への対応
    return null;
  }

  if (!rawValue) {
    return null;
  }

  let parsedData;
  try {
    parsedData = JSON.parse(rawValue);
  } catch (error) {
    // JSON解析に失敗した場合は不正データとして扱う
    return null;
  }

  if (!isValidDiagnosisData(parsedData)) {
    return null;
  }

  return parsedData;
}

/**
 * 診断データの構造が正しいかどうかを検証する。
 * @param {*} data
 * @returns {boolean}
 */
function isValidDiagnosisData(data) {
  if (!data || typeof data !== "object") {
    return false;
  }

  const hasValidAuraKey =
    typeof data.auraKey === "string" && Object.prototype.hasOwnProperty.call(auraResults, data.auraKey);

  const hasValidConcern =
    typeof data.concern === "string" && Object.prototype.hasOwnProperty.call(concernResults, data.concern);

  const hasValidBirthDate = typeof data.birthDate === "string" && data.birthDate.length > 0;

  // desiredFutureは空文字でも許容するため、文字列であることのみ確認
  const hasValidDesiredFuture = typeof data.desiredFuture === "string";

  return hasValidAuraKey && hasValidConcern && hasValidBirthDate && hasValidDesiredFuture;
}

/**
 * データが見つからない場合の案内画面を表示する。
 */
function showNoDataScreen() {
  const loadingScreen = document.getElementById("loadingScreen");
  const noDataScreen = document.getElementById("noDataScreen");
  const resultPage = document.getElementById("resultPage");

  if (loadingScreen) {
    loadingScreen.hidden = true;
  }
  if (resultPage) {
    resultPage.hidden = true;
  }
  if (noDataScreen) {
    noDataScreen.hidden = false;
  }
}

/**
 * 診断中演出を再生する。
 * メッセージを段階的に切り替え、プログレスバーを進行させる。
 * 演出終了後にコールバックを実行する。
 * @param {Function} onComplete - 演出終了後に呼び出す関数
 */
function playLoadingAnimation(onComplete) {
  const loadingScreen = document.getElementById("loadingScreen");
  const loadingMessageElement = document.getElementById("loadingMessage");
  const progressBarFill = document.getElementById("progressBarFill");
  const progressBar = document.getElementById("progressBar");

  // 演出の合計時間（2.5秒〜3.5秒の範囲で設定）
  const totalDurationMs = 3000;
  const messageCount = LOADING_MESSAGES.length;
  const intervalPerMessageMs = totalDurationMs / messageCount;

  let currentMessageIndex = 0;

  // 最初のメッセージを表示
  if (loadingMessageElement) {
    loadingMessageElement.textContent = LOADING_MESSAGES[currentMessageIndex];
  }

  // プログレスバーを滑らかに進行させる
  const progressStartTime = Date.now();
  const progressIntervalId = setInterval(() => {
    const elapsed = Date.now() - progressStartTime;
    const percentage = Math.min(100, Math.round((elapsed / totalDurationMs) * 100));

    if (progressBarFill) {
      progressBarFill.style.width = `${percentage}%`;
    }
    if (progressBar) {
      progressBar.setAttribute("aria-valuenow", String(percentage));
    }

    if (percentage >= 100) {
      clearInterval(progressIntervalId);
    }
  }, 100);

  // メッセージを段階的に切り替えるタイマー
  const messageIntervalId = setInterval(() => {
    currentMessageIndex += 1;
    if (currentMessageIndex < messageCount && loadingMessageElement) {
      loadingMessageElement.textContent = LOADING_MESSAGES[currentMessageIndex];
    }
  }, intervalPerMessageMs);

  // 演出終了処理
  setTimeout(() => {
    clearInterval(messageIntervalId);
    clearInterval(progressIntervalId);

    if (loadingScreen) {
      loadingScreen.classList.add("loading-screen-fade-out");
    }

    // フェードアウトの後に演出を隠し、結果描画へ進む
    setTimeout(() => {
      if (loadingScreen) {
        loadingScreen.hidden = true;
      }
      onComplete();
    }, 300);
  }, totalDurationMs);
}

/**
 * 結果画面を描画するメイン関数。
 * @param {Object} diagnosisData - sessionStorageから取得した診断データ
 */
function renderResult(diagnosisData) {
  const aura = auraResults[diagnosisData.auraKey];
  const concern = concernResults[diagnosisData.concern];

  if (!aura || !concern) {
    // データが不正な場合は安全側に倒してno-data画面を表示する
    showNoDataScreen();
    return;
  }

  // 色に応じたCSSクラスをbodyへ付与
  applyAuraThemeClass(diagnosisData.auraKey);

  // 8-2. 診断結果（色名表示）
  setTextContent("auraColorName", aura.colorName);

  // 8-3. 色別の導入文章
  renderFixedText("auraIntroduction", aura.introduction);

  // 8-4. 色別の秘められた金運
  renderFixedText("auraHiddenFortune", aura.hiddenFortune);

  // 8-5. 欲望の肯定
  renderFixedText("desireAffirmation", commonSections.desireAffirmation);

  // 8-6. 選択した悩みの文章
  setTextContent("concernTitleHeading", concern.title);
  renderFixedText("concernBody", concern.body);

  // 8-7. 自由記述した未来（自由記述が空欄の場合はセクション全体を非表示）
  renderDesiredFutureSection(diagnosisData.desiredFuture);

  // 8-8. 色別の未来想像
  renderFixedText("auraFutureVision", aura.futureVision);

  // 8-9. 色別の無料でできる一歩
  renderFixedText("auraFirstAction", aura.firstAction);

  // 8-10. 色別の金運が開花していない理由
  renderFixedText("auraUnopenedReason", aura.unopenedReason);

  // 8-11. 無料診断で分かる範囲（色名を動的に差し替え）
  renderFixedText("freeDiagnosisLimit", replaceColorPlaceholder(commonSections.freeDiagnosisLimit, aura.colorName));

  // 8-12. 個別金運開花鑑定の必要性
  renderFixedText("personalReadingNeed", replaceColorPlaceholder(commonSections.personalReadingNeed, aura.colorName));

  // 8-13. 鑑定で分かること
  renderFixedText("readingDetails", replaceColorPlaceholder(commonSections.readingDetails, aura.colorName));

  // 8-14. CTA直前の文章とリンク設定
  renderFixedText("ctaLead", commonSections.ctaLead);
  setupCtaLink();

  // 8-15. 注意事項
  renderFixedText("disclaimer", commonSections.disclaimer);

  // 演出終了後、結果本体をフェードインで表示
  const resultPage = document.getElementById("resultPage");
  if (resultPage) {
    resultPage.hidden = false;
    // フェードインのため、わずかに遅延させてからクラスを付与
    requestAnimationFrame(() => {
      resultPage.classList.add("result-visible");
    });
  }
}

/**
 * bodyへオーラカラーに応じたCSSクラスを付与する。
 * @param {string} auraKey - オーラのキー（例: "roseGold"）
 */
function applyAuraThemeClass(auraKey) {
  const body = document.getElementById("resultBody");
  if (!body) {
    return;
  }

  // オーラキーからCSSクラス名（ケバブケース）へ変換するマップ
  const auraKeyToClassName = {
    crimson: "aura-crimson",
    silver: "aura-silver",
    amber: "aura-amber",
    jade: "aura-jade",
    blue: "aura-blue",
    roseGold: "aura-rose-gold",
    indigo: "aura-indigo",
    gold: "aura-gold",
    purple: "aura-purple"
  };

  const className = auraKeyToClassName[auraKey];
  if (className) {
    body.classList.add(className);
  }
}

