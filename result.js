/**
 * 固定文章（開発者が用意した文章）を、指定要素へ安全なHTMLとして描画する。
 * @param {string} elementId - 描画先要素のID
 * @param {string} text - 表示するテキスト
 */
function renderFixedText(elementId, text) {
  const element = document.getElementById(elementId);
  if (!element) {
    return;
  }
  element.innerHTML = formatFixedText(text);
}

/**
 * 固定文章を、改行や箇条書きを保ったまま安全なHTMLへ変換する。
 * 段落（空行区切り）ごとに<p>、行内の改行は<br>、
 * 「* 」で始まる連続行は<ul><li>へ変換する。
 * @param {string} text - 変換対象の文章
 * @returns {string} 安全なHTML文字列
 */
function formatFixedText(text) {
  if (!text) {
    return "";
  }

  const trimmedText = text.trim();
  const paragraphs = trimmedText.split(/\n{2,}/);

  return paragraphs
    .map((paragraph) => paragraphToHtml(paragraph))
    .join("");
}

/**
 * 一つの段落（空行で区切られた塊）をHTMLへ変換する。
 * 段落内のすべての行が「* 」で始まる場合は箇条書きとして扱う。
 * @param {string} paragraph
 * @returns {string}
 */
function paragraphToHtml(paragraph) {
  const lines = paragraph.split("\n");
  const isListBlock = lines.every((line) => /^\*\s+/.test(line.trim()) || line.trim() === "");
  const nonEmptyLines = lines.filter((line) => line.trim() !== "");
  const isAllListItems = nonEmptyLines.length > 0 && nonEmptyLines.every((line) => /^\*\s+/.test(line.trim()));

  if (isListBlock && isAllListItems) {
    const listItems = nonEmptyLines
      .map((line) => {
        const itemText = line.trim().replace(/^\*\s+/, "");
        return `<li>${escapeHtml(itemText)}</li>`;
      })
      .join("");
    return `<ul class="text-list">${listItems}</ul>`;
  }

  const escapedParagraph = escapeHtml(paragraph);
  return `<p>${escapedParagraph.replace(/\n/g, "<br>")}</p>`;
}

/**
 * HTML特殊文字をエスケープする。
 * 固定文章であっても、安全のため必ずエスケープしてから使用する。
 * @param {string} text
 * @returns {string}
 */
function escapeHtml(text) {
  const escapeMap = {
    "&": "&amp;",
    "<": "&lt;",
    ">": "&gt;",
    '"': "&quot;",
    "'": "&#039;"
  };
  return text.replace(/[&<>"']/g, (character) => escapeMap[character]);
}

/**
 * 共通文章内の色名プレースホルダーを、実際の色名へ置き換える。
 * @param {string} text - {{COLOR_NAME}}を含む文章
 * @param {string} colorName - 置き換える色名（例: "黄金色"）
 * @returns {string}
 */
function replaceColorPlaceholder(text, colorName) {
  return text.split("{{COLOR_NAME}}").join(colorName);
}

